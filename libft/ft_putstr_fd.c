#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (fd < 0 || !s)
		return ;
	if (*s == '\0')
		write(fd, "(nil)", 5);
	while (*s)
	{
		write(fd, s++, 1);
	}
}
